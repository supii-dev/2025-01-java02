package ch01.sec08;

public class Basic {
    public static void main(String[] args) {
        int x; // 정수값을 담을수있는 변수 X를 선언했다. 실수,주소값 담을수 없음
        x = 1; // x에 1을 넣는다. (초기화), 1은 리터럴 (값 그자체인것을 리터럴)
               // =은 대입연산자(쓰기작업, 변수 값이변한다.)
        int y = 2; // 변수 y를 선언 하였고 2로 초기화
        int sum = x+y; // 변수 sum을 선언하였고 x와 y를 더한 값을 초기화
        // int sum = 1 + 2;
        // int sum = 3;
        System.out.println(sum);
        System.out.println(3);
        }
}
